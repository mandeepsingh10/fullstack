// Conditionals

// if else statements
const num = 10;

if (num > 0) {
    console.log('Number is positive');
}
else if (num < 0) {
    console.log('Number is negative');
}
else{
    console.log('Number is zero');
}

// switch statement

const color = 'blue';

switch(color) {
    case 'red':
        console.log('Color is red');
        break
    case 'green':
        console.log('Color is green');
        break
    case 'blue':
        console.log('Color is blue');
        break
    default:
        console.log('It is not a color');     
}
